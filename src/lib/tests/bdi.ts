export type BDIInterpretation = {
  label: string;
  description: string;
};

export function calculateBDIScore(answers: number[]): number {
  return answers.reduce((sum, score) => sum + score, 0);
}

export function getCognitiveSubscale(answers: number[]): number {
  return answers.slice(0, 13).reduce((sum, score) => sum + score, 0);
}

export function getSomaticSubscale(answers: number[]): number {
  return answers.slice(13, 21).reduce((sum, score) => sum + score, 0);
}

export function interpretBDI(score: number): BDIInterpretation {
  if (score <= 9) {
    return {
      label: "Отсутствие депрессивных симптомов",
      description: "По сумме баллов депрессивные симптомы не выражены.",
    };
  }
  if (score <= 15) {
    return {
      label: "Лёгкая депрессия (субдепрессия)",
      description: "Наблюдаются лёгкие депрессивные симптомы.",
    };
  }
  if (score <= 19) {
    return {
      label: "Умеренная депрессия",
      description: "Выраженность симптомов соответствует умеренной депрессии.",
    };
  }
  if (score <= 29) {
    return {
      label: "Выраженная депрессия (средней тяжести)",
      description: "Симптомы выражены и могут существенно влиять на повседневную жизнь.",
    };
  }
  return {
    label: "Тяжёлая депрессия",
    description: "Сумма баллов указывает на тяжёлую депрессию. Рекомендуется обратиться к специалисту.",
  };
}

export function buildBDIResult(answers: number[]) {
  const score = calculateBDIScore(answers);
  const interpretation = interpretBDI(score);
  const cognitive = getCognitiveSubscale(answers);
  const somatic = getSomaticSubscale(answers);

  return {
    testId: "bdi",
    testTitle: "Шкала депрессии Бека (BDI)",
    completedAt: "",
    summary: `${score} из 62 баллов — ${interpretation.label}. ${interpretation.description}`,
    details: [
      { label: "Суммарный балл", value: `${score} / 62` },
      { label: "Уровень", value: interpretation.label },
      {
        label: "Когнитивно-аффективная субшкала (пункты 1–13)",
        value: `${cognitive} / 39`,
      },
      {
        label: "Соматическая субшкала (пункты 14–21)",
        value: `${somatic} / 24`,
      },
    ],
    answers,
  };
}

export function isValidBDIAnswers(answers: unknown): answers is number[] {
  return (
    Array.isArray(answers) &&
    answers.length === 21 &&
    answers.every((value) => typeof value === "number" && value >= 0 && value <= 3)
  );
}
